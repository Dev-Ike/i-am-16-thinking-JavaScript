const arr = [1, 2, 3, 4, 5, 6 , 7]; // This is an array of numbers

const incrementedArr = arr.map((element) => element + 1); // This iterates through the array using the map() method to increment all the numbers in the array by 1

console.log("before : ", arr); // This console.log the array
console.log("after : ", incrementedArr); // This console.log the incremented array